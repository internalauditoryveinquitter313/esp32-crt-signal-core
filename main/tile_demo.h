/* Simple demo nametable: 32 cols x 32 rows (PoT pitch), 30 rows visible.
 * Layout:
 *   Row 0 (top edge):      border frame (6) all across
 *   Rows 1-2:              horizontal bar (4)
 *   Rows 3-4:              gradient (7)
 *   Rows 5-24:             alternating diagonal (3) and checkerboard (5)
 *   Rows 25-26:            horizontal bar (4)
 *   Row 27-28:             gradient (7)
 *   Row 29 (bottom):       border frame (6)
 * All 30 visible rows together stitch a test card that exercises every
 * pattern tile at least once. Unused pitch rows 30-31 stay zero. */

static inline void tile_demo_fill_nametable(uint8_t *nt, uint16_t pitch_w)
{
    for (uint16_t r = 0; r < 32; ++r) {
        uint8_t tile;
        if (r == 0 || r == 29)
            tile = 6; /* border frame */
        else if (r == 1 || r == 2 || r == 25 || r == 26)
            tile = 4; /* horizontal bar */
        else if (r == 3 || r == 4 || r == 27 || r == 28)
            tile = 7; /* gradient */
        else if (r >= 30)
            tile = 0; /* off-screen */
        else
            tile = ((r & 1u) == 0) ? 3u : 5u;
        for (uint16_t c = 0; c < pitch_w; ++c) {
            nt[(size_t)r * pitch_w + c] = tile;
        }
    }
}
