def extract_channels_yuv(img):
    """Separate Y, U, V channels — the native domain of composite video."""
    yuv = cv2.cvtColor(img, cv2.COLOR_BGR2YUV)
    y, u, v = cv2.split(yuv)
    return {
        "Y_luma": y,
        "U_chroma": u,
        "V_chroma": v,
    }


def extract_channels_yiq(img):
    """Convert to YIQ — the actual NTSC modulation color space.

    I and Q are what the NTSC subcarrier physically encodes.
    I = orange-cyan axis, Q = purple-green axis.
    IQ are UV rotated by 33 degrees.
    """
    bgr_f = img.astype(np.float64) / 255.0
    r, g, b = bgr_f[:, :, 2], bgr_f[:, :, 1], bgr_f[:, :, 0]

    y = 0.299 * r + 0.587 * g + 0.114 * b
    i = 0.5957 * r - 0.2745 * g - 0.3213 * b
    q = 0.2115 * r - 0.5226 * g + 0.3111 * b

    # Normalize I and Q from their ranges to 0-255 for visualization
    i_vis = np.clip((i + 0.5957) / (2 * 0.5957) * 255, 0, 255).astype(np.uint8)
    q_vis = np.clip((q + 0.5226) / (2 * 0.5226) * 255, 0, 255).astype(np.uint8)
    y_vis = np.clip(y * 255, 0, 255).astype(np.uint8)

    return {
        "Y_luma": y_vis,
        "I_inphase": i_vis,
        "Q_quadrature": q_vis,
        "_raw_y": y,
        "_raw_i": i,
        "_raw_q": q,
    }


def extract_channels_rgb(img):
    """Separate R, G, B channels — useful to check phosphor excitation."""
    b, g, r = cv2.split(img)
    return {"R": r, "G": g, "B": b}


def extract_channels_hsv(img):
    """Separate H, S, V — hue is key for chroma phase analysis."""
    hsv = cv2.cvtColor(img, cv2.COLOR_BGR2HSV)
    h, s, v = cv2.split(hsv)
    return {"Hue": h, "Saturation": s, "Value": v}


