def fft_magnitude_spectrum(img):
    """2D FFT magnitude spectrum — reveals periodic patterns.

    Bright spots in the spectrum = periodic structures:
    - Horizontal lines = scanline frequency
    - Diagonal patterns = moire from phosphor grid
    - Isolated peaks = subcarrier-related patterns (if visible)
    """
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY).astype(np.float32)

    # Pad to optimal FFT size
    rows, cols = gray.shape
    opt_rows = cv2.getOptimalDFTSize(rows)
    opt_cols = cv2.getOptimalDFTSize(cols)
    padded = np.zeros((opt_rows, opt_cols), dtype=np.float32)
    padded[:rows, :cols] = gray

    # FFT
    dft = np.fft.fft2(padded)
    dft_shift = np.fft.fftshift(dft)

    magnitude = np.abs(dft_shift)
    # Log scale for visualization
    magnitude = np.log1p(magnitude)

    # Normalize to 0-255
    magnitude = (magnitude / magnitude.max() * 255).astype(np.uint8)

    # Apply colormap for better visibility
    colored = cv2.applyColorMap(magnitude, cv2.COLORMAP_INFERNO)

    return colored


def fft_per_channel(img):
    """FFT magnitude spectrum for each color channel separately.

    Different channels may show different frequency content — the chroma
    subcarrier pattern affects R and B differently than G.
    """
    results = {}
    for ch_name, ch_idx in [("B", 0), ("G", 1), ("R", 2)]:
        channel = img[:, :, ch_idx].astype(np.float32)

        dft = np.fft.fft2(channel)
        dft_shift = np.fft.fftshift(dft)
        magnitude = np.log1p(np.abs(dft_shift))
        magnitude = (magnitude / magnitude.max() * 255).astype(np.uint8)
        results[ch_name] = cv2.applyColorMap(magnitude, cv2.COLORMAP_INFERNO)

    return results


def fft_notch_demoire(img, notch_radius=5, threshold_pct=99.5):
    """Remove moire patterns using FFT notch filtering.

    Automatically detects strong periodic peaks (likely moire from
    phosphor grid / scanlines) and suppresses them.
    """
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY).astype(np.float32)
    rows, cols = gray.shape

    dft = np.fft.fft2(gray)
    dft_shift = np.fft.fftshift(dft)
    magnitude = np.abs(dft_shift)

    # Find peaks above threshold (excluding DC)
    center_y, center_x = rows // 2, cols // 2
    threshold = np.percentile(magnitude, threshold_pct)

    # Create mask (1 = keep, 0 = suppress)
    mask = np.ones_like(magnitude)

    for y in range(rows):
        for x in range(cols):
            if magnitude[y, x] > threshold:
                dist = np.sqrt((y - center_y)**2 + (x - center_x)**2)
                if dist > 10:  # skip DC component
                    # Notch out this frequency
                    cv2.circle(mask, (x, y), notch_radius, 0, -1)
                    # Also notch the conjugate
                    cx = 2 * center_x - x
                    cy = 2 * center_y - y
                    if 0 <= cx < cols and 0 <= cy < rows:
                        cv2.circle(mask, (cx, cy), notch_radius, 0, -1)

    # Apply mask
    dft_shift *= mask
    dft_ishift = np.fft.ifftshift(dft_shift)
    result = np.abs(np.fft.ifft2(dft_ishift))
    result = np.clip(result, 0, 255).astype(np.uint8)

    return result


