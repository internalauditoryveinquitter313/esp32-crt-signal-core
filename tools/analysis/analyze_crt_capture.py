def save_img(img, output_dir: Path, name: str):
    path = output_dir / f"{name}.png"
    cv2.imwrite(str(path), img)
    print(f"  -> {path}")


def run_analysis(input_path: str, output_dir: str, standard: str = "ntsc",
                 quick: bool = False, run_all: bool = True):
    img = cv2.imread(input_path)
    if img is None:
        print(f"ERROR: cannot read {input_path}")
        sys.exit(1)

    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)

    stem = Path(input_path).stem

    print(f"=== CRT Capture Analyzer ===")
    print(f"Input:    {input_path} ({img.shape[1]}x{img.shape[0]})")
    print(f"Standard: {standard.upper()}")
    print(f"Output:   {out}/\n")

    # --- Dashboard (always) ---
    print("[1/11] Diagnostic dashboard...")
    save_img(create_diagnostic_dashboard(img), out, f"{stem}_dashboard")

    # --- Enhancement ---
    print("[2/11] CLAHE enhancement...")
    save_img(enhance_clahe(img), out, f"{stem}_clahe")

    if not quick:
        print("[2b/11] Multi-Scale Retinex (NASA)...")
        save_img(enhance_multiscale_retinex(img), out, f"{stem}_retinex")

        print("[2c/11] Unsharp mask...")
        save_img(enhance_unsharp_mask(img), out, f"{stem}_unsharp")

    # --- False color ---
    print("[3/11] False color heatmaps...")
    save_img(false_color_luma(img), out, f"{stem}_false_color_jet")
    save_img(false_color_luma(img, cv2.COLORMAP_TURBO), out, f"{stem}_false_color_turbo")
    if not quick:
        save_img(false_color_ire_zones(img), out, f"{stem}_ire_zones")

    # --- Channel separation ---
    print("[4/11] Channel separation (YUV)...")
    yuv = extract_channels_yuv(img)
    for name, ch in yuv.items():
        save_img(ch, out, f"{stem}_{name}")

    if not quick:
        print("[4b/11] Channel separation (YIQ — NTSC native)...")
        yiq = extract_channels_yiq(img)
        for name, ch in yiq.items():
            if not name.startswith("_"):
                save_img(ch, out, f"{stem}_{name}")

        print("[4c/11] Channel separation (RGB)...")
        rgb = extract_channels_rgb(img)
        for name, ch in rgb.items():
            save_img(ch, out, f"{stem}_{name}")

        print("[4d/11] Channel separation (HSV)...")
        hsv_ch = extract_channels_hsv(img)
        for name, ch in hsv_ch.items():
            save_img(ch, out, f"{stem}_{name}")

    # --- Vectorscope ---
    print("[5/11] Software vectorscope...")
    save_img(software_vectorscope(img), out, f"{stem}_vectorscope")

    # --- Hue angle measurement ---
    print("[6/11] Bar hue angle measurement...")
    measurements = measure_bar_hue_angles(img)
    save_img(draw_bar_analysis_overlay(img, measurements), out, f"{stem}_bar_overlay")

    print("\n  === COLOR BAR MEASUREMENTS ===")
    print(f"  {'Bar':<10} {'IRE':>6} {'YIQ Phase':>10} {'Ref Phase':>10} {'Phase Err':>10} {'Chroma':>8}")
    print(f"  {'-'*10} {'-'*6} {'-'*10} {'-'*10} {'-'*10} {'-'*8}")
    for m in measurements:
        ref_ph = f"{m['ref_phase']:.1f}" if m["ref_phase"] else "  ---"
        err = f"{m['phase_error']:+.1f}" if m["phase_error"] is not None else "  ---"
        print(f"  {m['bar']:<10} {m['approx_ire']:>5.1f} "
              f"{m['yiq_phase_deg']:>9.1f} {ref_ph:>10} {err:>10} {m['yiq_chroma_amp']:>7.4f}")
    print()

    # --- Waveform ---
    print("[7/11] Waveform monitor...")
    save_img(software_waveform(img), out, f"{stem}_waveform")

    # --- FFT ---
    print("[8/11] FFT magnitude spectrum...")
    save_img(fft_magnitude_spectrum(img), out, f"{stem}_fft_spectrum")

    if not quick:
        print("[8b/11] Per-channel FFT...")
        fft_ch = fft_per_channel(img)
        for name, spectrum in fft_ch.items():
            save_img(spectrum, out, f"{stem}_fft_{name}")

        print("[8c/11] FFT demoire...")
        save_img(fft_notch_demoire(img), out, f"{stem}_demoire")

    # --- Scanlines ---
    print("[9/11] Scanline analysis...")
    scan_info = extract_scanlines(img)
    if scan_info["dominant_period_px"]:
        print(f"  Detected scanline period: {scan_info['dominant_period_px']} px")
    else:
        print("  No clear scanline periodicity detected")

    # --- Edges ---
    print("[10/11] Edge / transition analysis...")
    edge_info = analyze_bar_transitions(img)
    save_img(edge_info["edge_map"], out, f"{stem}_edges")
    print(f"  Mean gradient at transitions: {edge_info['mean_gradient_at_transitions']:.1f}")

    # --- Histogram ---
    print("[11/11] Detailed histogram...")
    save_img(detailed_histogram(img), out, f"{stem}_histogram")

    print(f"\nDone! All outputs in: {out}/")


def main():
    parser = argparse.ArgumentParser(
        description="CRT Capture Analyzer — NASA-style post-processing for signal diagnostics"
    )
    parser.add_argument("input", help="Input image (PNG/JPG from webcam capture)")
    parser.add_argument("--output-dir", "-o", default="./crt_analysis",
                        help="Output directory (default: ./crt_analysis)")
    parser.add_argument("--standard", "-s", choices=["ntsc", "pal"], default="ntsc",
                        help="Video standard (default: ntsc)")
    parser.add_argument("--quick", "-q", action="store_true",
                        help="Quick mode — skip slow analyses")
    parser.add_argument("--all", "-a", action="store_true",
                        help="Run all analyses (default)")

    args = parser.parse_args()
    run_analysis(args.input, args.output_dir, args.standard, args.quick, args.all)


if __name__ == "__main__":
    main()
