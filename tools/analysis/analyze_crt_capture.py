def measure_bar_hue_angles(img, num_bars=8, margin_pct=0.15):
    """Measure the actual hue angle of each color bar region.

    Computes both:
    - HSV hue (perceptual color wheel)
    - YIQ chrominance phase (NTSC subcarrier angle via atan2(Q, I))

    Returns a list of dicts with measurements per bar.
    """
    h, w = img.shape[:2]

    # Define ROI for the bars region (top 2/3, excluding ramp at bottom)
    bar_top = int(h * 0.1)
    bar_bottom = int(h * 0.6)

    # Extract bar regions
    bar_width = w // num_bars
    margin = int(bar_width * margin_pct)

    # Convert full image
    hsv = cv2.cvtColor(img, cv2.COLOR_BGR2HSV)
    bgr_f = img.astype(np.float64) / 255.0
    r = bgr_f[:, :, 2]
    g = bgr_f[:, :, 1]
    b = bgr_f[:, :, 0]

    # YIQ conversion
    y_raw = 0.299 * r + 0.587 * g + 0.114 * b
    i_raw = 0.5957 * r - 0.2745 * g - 0.3213 * b
    q_raw = 0.2115 * r - 0.5226 * g + 0.3111 * b

    bar_names = ["white", "yellow", "cyan", "green", "magenta", "red", "blue", "black"]
    results = []

    for idx in range(num_bars):
        x_start = idx * bar_width + margin
        x_end = (idx + 1) * bar_width - margin

        roi_hsv = hsv[bar_top:bar_bottom, x_start:x_end]
        roi_y = y_raw[bar_top:bar_bottom, x_start:x_end]
        roi_i = i_raw[bar_top:bar_bottom, x_start:x_end]
        roi_q = q_raw[bar_top:bar_bottom, x_start:x_end]
        roi_bgr = img[bar_top:bar_bottom, x_start:x_end]

        # HSV hue (OpenCV uses 0-179, multiply by 2 for 0-360)
        hue_mean = np.mean(roi_hsv[:, :, 0]) * 2.0
        sat_mean = np.mean(roi_hsv[:, :, 1])
        val_mean = np.mean(roi_hsv[:, :, 2])

        # YIQ chrominance phase angle
        i_mean = np.mean(roi_i)
        q_mean = np.mean(roi_q)
        chroma_phase = np.degrees(np.arctan2(q_mean, i_mean)) % 360

        # Chrominance amplitude
        chroma_amp = np.sqrt(i_mean**2 + q_mean**2)

        # Luma (approximate IRE)
        luma_mean = np.mean(roi_y)
        approx_ire = luma_mean * 92.5 + 7.5  # rough mapping 0->7.5, 1->100

        # Mean BGR
        mean_bgr = np.mean(roi_bgr.reshape(-1, 3), axis=0)

        name = bar_names[idx] if idx < len(bar_names) else f"bar_{idx}"
        ref = NTSC_75_BARS.get(name, {})

        result = {
            "bar": name,
            "hsv_hue_deg": round(hue_mean, 1),
            "hsv_sat": round(sat_mean, 1),
            "hsv_val": round(val_mean, 1),
            "yiq_phase_deg": round(chroma_phase, 1),
            "yiq_chroma_amp": round(chroma_amp, 4),
            "approx_ire": round(approx_ire, 1),
            "mean_bgr": [round(x, 1) for x in mean_bgr.tolist()],
            "ref_phase": ref.get("phase"),
            "ref_ire": ref.get("ire"),
            "phase_error": None,
        }

        if ref.get("phase") is not None and chroma_amp > 0.01:
            err = chroma_phase - ref["phase"]
            # Wrap to -180..180
            err = (err + 180) % 360 - 180
            result["phase_error"] = round(err, 1)

        results.append(result)

    return results


def draw_bar_analysis_overlay(img, measurements):
    """Draw measured values on top of the original image."""
    overlay = img.copy()
    h, w = overlay.shape[:2]
    num_bars = len(measurements)
    bar_width = w // num_bars

    for idx, m in enumerate(measurements):
        x = idx * bar_width + bar_width // 2
        y_base = int(h * 0.65)

        # Bar name
        cv2.putText(overlay, m["bar"].upper(),
                    (x - 25, y_base),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.4, (255, 255, 255), 1)

        # IRE level
        cv2.putText(overlay, f"{m['approx_ire']:.0f} IRE",
                    (x - 25, y_base + 18),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.35, (200, 200, 200), 1)

        # Phase angle (skip achromatic bars)
        if m["ref_phase"] is not None:
            color = (0, 255, 0) if abs(m["phase_error"] or 999) < 10 else (0, 0, 255)
            cv2.putText(overlay, f"Ph:{m['yiq_phase_deg']:.0f}",
                        (x - 25, y_base + 34),
                        cv2.FONT_HERSHEY_SIMPLEX, 0.35, color, 1)
            if m["phase_error"] is not None:
                cv2.putText(overlay, f"Err:{m['phase_error']:+.1f}",
                            (x - 25, y_base + 48),
                            cv2.FONT_HERSHEY_SIMPLEX, 0.3, color, 1)

    return overlay


