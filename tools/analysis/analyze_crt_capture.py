def analyze_bar_transitions(img):
    """Measure the sharpness of color bar transitions.

    Sharp transitions = good bandwidth. Blurry = bandwidth limiting or
    poor camera focus. Ringing = overshoot from the webcam's processing.
    """
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
    h, w = gray.shape

    # Take a horizontal slice through the middle of the bars
    mid_y = h // 3
    line = gray[mid_y, :].astype(np.float32)

    # Compute gradient (derivative)
    gradient = np.gradient(line)

    # Find transition points (large gradient magnitude)
    threshold = np.std(gradient) * 2
    transitions = np.where(np.abs(gradient) > threshold)[0]

    # Sobel edge detection for visualization
    sobel_x = cv2.Sobel(gray, cv2.CV_64F, 1, 0, ksize=3)
    sobel_y = cv2.Sobel(gray, cv2.CV_64F, 0, 1, ksize=3)
    edges = np.sqrt(sobel_x**2 + sobel_y**2)
    edges = np.clip(edges / edges.max() * 255, 0, 255).astype(np.uint8)

    return {
        "edge_map": edges,
        "line_profile": line,
        "gradient": gradient,
        "transition_positions": transitions,
        "mean_gradient_at_transitions": float(np.mean(np.abs(gradient[transitions]))) if len(transitions) > 0 else 0,
    }


