def enhance_clahe(img, clip_limit=3.0, grid_size=(8, 8)):
    """CLAHE on luminance channel — reveals subtle luma variations."""
    lab = cv2.cvtColor(img, cv2.COLOR_BGR2LAB)
    l, a, b = cv2.split(lab)
    clahe = cv2.createCLAHE(clipLimit=clip_limit, tileGridSize=grid_size)
    l_enhanced = clahe.apply(l)
    enhanced = cv2.merge([l_enhanced, a, b])
    return cv2.cvtColor(enhanced, cv2.COLOR_LAB2BGR)


def enhance_multiscale_retinex(img, sigmas=(15, 80, 250)):
    """Multi-Scale Retinex — NASA Langley technique for dynamic range compression.

    Simultaneously provides:
    - Dynamic range compression (see into shadows and highlights)
    - Color constancy (normalize illuminant effects from webcam)
    - Detail enhancement in both bright and dark regions
    """
    img_f = img.astype(np.float64) + 1.0  # avoid log(0)
    retinex = np.zeros_like(img_f)

    for sigma in sigmas:
        blur = cv2.GaussianBlur(img_f, (0, 0), sigma)
        retinex += np.log10(img_f) - np.log10(blur + 1.0)

    retinex /= len(sigmas)

    # Normalize to 0-255
    for c in range(3):
        channel = retinex[:, :, c]
        lo, hi = np.percentile(channel, [1, 99])
        retinex[:, :, c] = np.clip((channel - lo) / (hi - lo + 1e-6) * 255, 0, 255)

    return retinex.astype(np.uint8)


def enhance_unsharp_mask(img, sigma=2.0, strength=1.5):
    """Unsharp mask — sharpens edges to reveal signal transitions."""
    blurred = cv2.GaussianBlur(img, (0, 0), sigma)
    sharpened = cv2.addWeighted(img, 1.0 + strength, blurred, -strength, 0)
    return np.clip(sharpened, 0, 255).astype(np.uint8)


