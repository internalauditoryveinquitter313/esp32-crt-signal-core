# 11. COMPOSITE: DIAGNOSTIC DASHBOARD
# ---------------------------------------------------------------------------

def create_diagnostic_dashboard(img):
    """Generate a single composite image with all key diagnostic views.

    Layout (2x3 grid):
    [Original  ] [CLAHE Enhanced] [False Color ]
    [Waveform  ] [Vectorscope   ] [FFT Spectrum]
    """
    target_h, target_w = 360, 480

    def resize(src):
        return cv2.resize(src, (target_w, target_h))

    original = resize(img)
    clahe = resize(enhance_clahe(img))
    fc = resize(false_color_luma(img))
    wf = resize(software_waveform(img, height=target_h))
    vs = resize(software_vectorscope(img, size=max(target_h, target_w)))
    fft = resize(fft_magnitude_spectrum(img))

    # Labels
    font = cv2.FONT_HERSHEY_SIMPLEX
    for panel, label in [(original, "ORIGINAL"), (clahe, "CLAHE"),
                         (fc, "FALSE COLOR"), (wf, "WAVEFORM"),
                         (vs, "VECTORSCOPE"), (fft, "FFT SPECTRUM")]:
        cv2.putText(panel, label, (8, 20), font, 0.5, (0, 255, 255), 1)

    top_row = np.hstack([original, clahe, fc])
    bottom_row = np.hstack([wf, vs, fft])
    dashboard = np.vstack([top_row, bottom_row])

    return dashboard


