# 8. SCANLINE ANALYSIS
# ---------------------------------------------------------------------------

def extract_scanlines(img, min_period=2, max_period=10):
    """Detect and extract CRT scanline positions from the capture.

    Uses horizontal gradient to find the periodic bright/dark pattern.
    """
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY).astype(np.float32)

    # Average across columns to get vertical brightness profile
    v_profile = np.mean(gray, axis=1)

    # Find the dominant vertical frequency (scanline period)
    fft_v = np.abs(np.fft.rfft(v_profile - np.mean(v_profile)))
    freqs = np.fft.rfftfreq(len(v_profile))

    # Look for peaks in the expected scanline frequency range
    period_range = (freqs > 1.0 / max_period) & (freqs < 1.0 / min_period)
    if np.any(period_range):
        peak_idx = np.argmax(fft_v * period_range)
        scanline_freq = freqs[peak_idx]
        scanline_period = 1.0 / scanline_freq if scanline_freq > 0 else 0

        return {
            "vertical_profile": v_profile,
            "fft_magnitudes": fft_v,
            "frequencies": freqs,
            "dominant_period_px": round(scanline_period, 2),
            "dominant_freq": round(scanline_freq, 4),
        }

    return {
        "vertical_profile": v_profile,
        "fft_magnitudes": fft_v,
        "frequencies": freqs,
        "dominant_period_px": None,
        "dominant_freq": None,
    }


