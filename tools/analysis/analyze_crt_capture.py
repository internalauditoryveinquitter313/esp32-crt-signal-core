def software_vectorscope(img, size=512):
    """Generate a vectorscope plot from the image — like a broadcast monitor.

    Plots Cb vs Cr (or U vs V) for every pixel. Color bar dots should cluster
    at known positions. Phase errors show as rotated clusters.
    """
    yuv = cv2.cvtColor(img, cv2.COLOR_BGR2YUV)
    _, u, v = cv2.split(yuv)

    scope = np.zeros((size, size, 3), dtype=np.uint8)

    # Draw crosshairs and graticule
    center = size // 2
    cv2.line(scope, (center, 0), (center, size), (40, 40, 40), 1)
    cv2.line(scope, (0, center), (size, center), (40, 40, 40), 1)

    # Draw target boxes for 75% NTSC bars
    scale = size / 256.0
    bar_targets = {
        "Yl": (44, 136),   "Cy": (166, 16),  "Gn": (38, 16),
        "Mg": (218, 240), "Rd": (90, 240), "Bl": (218, 120),
    }
    for name, (u_val, v_val) in bar_targets.items():
        x = int(u_val * scale)
        y = int((255 - v_val) * scale)
        cv2.rectangle(scope, (x - 8, y - 8), (x + 8, y + 8), (0, 80, 0), 1)
        cv2.putText(scope, name, (x + 10, y + 4),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.35, (0, 120, 0), 1)

    # Plot pixels
    u_flat = u.flatten().astype(np.float32)
    v_flat = v.flatten().astype(np.float32)

    # Subsample for performance (C270 is 1280x960 max)
    step = max(1, len(u_flat) // 500000)
    u_flat = u_flat[::step]
    v_flat = v_flat[::step]

    x_coords = (u_flat * scale).astype(np.int32)
    y_coords = ((255 - v_flat) * scale).astype(np.int32)

    # Clamp
    x_coords = np.clip(x_coords, 0, size - 1)
    y_coords = np.clip(y_coords, 0, size - 1)

    # Accumulate with color
    for x, y in zip(x_coords, y_coords):
        scope[y, x] = np.minimum(scope[y, x].astype(np.int32) + [2, 6, 2], 255).astype(np.uint8)

    return scope


