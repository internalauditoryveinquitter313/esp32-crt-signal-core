def detailed_histogram(img):
    """Generate per-channel and luma histograms with reference markers.

    The histogram shape reveals:
    - Peaks at specific levels = color bar luma values (good)
    - Broad spread = noise or poor contrast
    - Clipping at 0 or 255 = webcam exposure issues
    """
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)

    hist_canvas = np.zeros((300, 512, 3), dtype=np.uint8)

    # Luma histogram
    hist_gray = cv2.calcHist([gray], [0], None, [256], [0, 256]).flatten()
    hist_gray = hist_gray / hist_gray.max() * 280

    for x in range(256):
        h = int(hist_gray[x])
        cv2.line(hist_canvas, (x * 2, 299), (x * 2, 299 - h), (200, 200, 200), 1)
        cv2.line(hist_canvas, (x * 2 + 1, 299), (x * 2 + 1, 299 - h), (200, 200, 200), 1)

    # Per-channel histograms
    colors = [(255, 0, 0), (0, 255, 0), (0, 0, 255)]
    for ch, color in enumerate(colors):
        hist = cv2.calcHist([img], [ch], None, [256], [0, 256]).flatten()
        hist = hist / hist.max() * 280
        for x in range(256):
            h = int(hist[x])
            cv2.line(hist_canvas, (x * 2, 299), (x * 2, 299 - h), color, 1)

    return hist_canvas


