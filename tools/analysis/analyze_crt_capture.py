def false_color_luma(img, colormap=cv2.COLORMAP_JET):
    """False color map of luminance — like broadcast waveform monitor colors.

    Maps luma levels to a thermal-style colormap so you can visually distinguish
    IRE levels that look identical in grayscale.
    """
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
    return cv2.applyColorMap(gray, colormap)


def false_color_ire_zones(img):
    """Custom false color with IRE-zone boundaries marked.

    Zones:
    - Super black  (< 7.5 IRE):  deep blue
    - Black setup  (7.5 IRE):    dark gray
    - Low (7.5-30 IRE):          blue->cyan
    - Mid (30-70 IRE):           green->yellow
    - High (70-100 IRE):         orange->red
    - Super white (>100 IRE):    magenta (illegal)
    """
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY).astype(np.float32)

    # Map webcam luminance to approximate IRE (assumes gamma ~2.2 capture)
    # Normalize: 0->0 IRE, 255->~110 IRE
    ire = (gray / 255.0) * 110.0

    result = np.zeros((*gray.shape, 3), dtype=np.uint8)

    # Super black (< 7.5 IRE)
    mask = ire < 7.5 * 0.95
    result[mask] = [180, 0, 0]  # blue

    # Setup (around 7.5 IRE)
    mask = (ire >= 7.5 * 0.95) & (ire < 7.5 * 1.1)
    result[mask] = [80, 80, 80]

    # Low (7.5-30 IRE)
    mask = (ire >= 7.5 * 1.1) & (ire < 30)
    t = np.clip((ire[mask] - 7.5) / (30 - 7.5), 0, 1)
    result[mask] = np.stack([
        (200 * (1 - t)).astype(np.uint8),
        (200 * t).astype(np.uint8),
        np.zeros_like(t, dtype=np.uint8)
    ], axis=-1)

    # Mid (30-70 IRE)
    mask = (ire >= 30) & (ire < 70)
    t = np.clip((ire[mask] - 30) / (70 - 30), 0, 1)
    result[mask] = np.stack([
        np.zeros_like(t, dtype=np.uint8),
        (255 * (1 - t * 0.5)).astype(np.uint8),
        (255 * t).astype(np.uint8)
    ], axis=-1)

    # High (70-100 IRE)
    mask = (ire >= 70) & (ire < 100)
    t = np.clip((ire[mask] - 70) / (100 - 70), 0, 1)
    result[mask] = np.stack([
        np.zeros_like(t, dtype=np.uint8),
        (128 * (1 - t)).astype(np.uint8),
        np.full_like(t, 255, dtype=np.uint8)
    ], axis=-1)

    # Super white (> 100 IRE — illegal in NTSC)
    mask = ire >= 100
    result[mask] = [200, 0, 200]  # magenta

    return result


