def software_waveform(img, height=256):
    """Generate a waveform monitor display — plots luma per column.

    Each column of the output corresponds to a column of the input.
    Pixel brightness is plotted vertically (0 at bottom, 255 at top).
    """
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
    h, w = gray.shape
    scope = np.zeros((height, w, 3), dtype=np.uint8)

    for col in range(w):
        column = gray[:, col]
        for val in column:
            y = height - 1 - int(val * (height - 1) / 255)
            y = max(0, min(y, height - 1))
            scope[y, col] = np.minimum(
                scope[y, col].astype(np.int32) + [1, 4, 1], 255
            ).astype(np.uint8)

    # Draw IRE reference lines
    for ire, label in [(7.5, "7.5"), (50, "50"), (100, "100")]:
        y_pos = height - 1 - int((ire / 110.0) * (height - 1))
        y_pos = max(0, min(y_pos, height - 1))
        scope[y_pos, :] = np.maximum(scope[y_pos, :], [30, 30, 0])
        cv2.putText(scope, f"{label} IRE", (4, y_pos - 2),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.3, (0, 60, 60), 1)

    return scope


def luma_line_profile(img, line_y=None):
    """Extract a single horizontal line profile of luma values.

    Returns (x_array, luma_array) — plot these to see the exact waveform
    of one scanline as the CRT would trace it.
    """
    gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
    if line_y is None:
        line_y = gray.shape[0] // 3  # default to upper third

    line = gray[line_y, :].astype(np.float32)
    x = np.arange(len(line))
    return x, line


